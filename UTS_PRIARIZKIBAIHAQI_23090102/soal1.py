bilangan_1 = int(input("masukan bilangan pertama : "))
bilangan_2 = int(input("masukan bilangan kedua : "))

pemjumlahan = bilangan_1 + bilangan_2
pengurangan = bilangan_1 - bilangan_2
modulus = bilangan_1 % bilangan_2

print(f"hasil penjumlahan dari {bilangan_1} dan {bilangan_2} adalah :{pemjumlahan}")
print(f"hasil pengurangan dari {bilangan_1} dan {bilangan_2} adalah :{pengurangan}")
print(f"hasil modulus dari {bilangan_1} dan {bilangan_2} adalah :{modulus}")

print ("selesai")